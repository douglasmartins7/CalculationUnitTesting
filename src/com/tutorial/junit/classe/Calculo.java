package com.tutorial.junit.classe;

public class Calculo {

	public int somar(int a, int b) {
		int somar = a + b;
		return somar;
	}
	
	public int dividir(int a, int b) {
		int dividir = a / b;
		return dividir;
	}
	
	public int multiplicar(int a, int b) {
		int multiplicar = a * b;
		return multiplicar;
	}
	
	public int subtrair(int a,  int b) {
		int subtrair = a - b;
		return subtrair;
	}
}
